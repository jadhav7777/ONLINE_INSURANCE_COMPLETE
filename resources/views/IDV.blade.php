<p style="text-align: justify;">Insured Declared Value is the maximum Sum Assured fixed by the insurer which is provided on theft or total loss of vehicle. Basically, IDV is the current market value of the vehicle. If the vehicle suffers total loss, IDV is the compensation that the insurer will provide to the policyholder.</p>
<p style="text-align: justify;">IDV is calculated as manufacturer’s listed selling price minus depreciation. The registration and insurance cost are excluded from IDV. The IDV of the accessories which are not factory fitted, are calculated separately at extra cost if insurance is required for them.&nbsp;</p>
